    /**
     * Get client ip.
     *
     * @return string
     */
    public static function getClientIp()
    {
        return
            $_ENV['HTTP_CLIENT_IP'] ?:
                $_ENV['HTTP_X_FORWARDED_FOR'] ?:
                    $_ENV['HTTP_X_FORWARDED'] ?:
                        $_ENV['HTTP_FORWARDED_FOR'] ?:
                            $_ENV['HTTP_FORWARDED'] ?:
                                $_ENV['REMOTE_ADDR'];
    }

    /**
     * Get a random string.
     *
     * @param int $length
     * @return string
     */
    public static function getRandomString($length = 20)
    {
        $random = new \Phalcon\Encryption\Security\Random();

        return \substr($random->hex(300), 0, $length);
    }

    /**
     * Get a random uuid.
     *
     * @return string
     */
    public static function getUuid()
    {
        $random = new \Phalcon\Encryption\Security\Random();

        return $random->uuid();
    }

